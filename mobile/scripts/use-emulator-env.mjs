#!/usr/bin/env node
/**
 * Writes mobile/.env.local from a platform emulator template.
 * Optionally copies VITE_MAPBOX_ACCESS_TOKEN from client/.env.local when missing.
 *
 * Usage:
 *   node scripts/use-emulator-env.mjs ios
 *   node scripts/use-emulator-env.mjs android
 *   node scripts/use-emulator-env.mjs ios --dotnet
 *   node scripts/use-emulator-env.mjs show
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const mobileRoot = path.resolve(__dirname, '..');
const clientEnvLocal = path.resolve(mobileRoot, '../client/.env.local');

const PLATFORM_FILES = {
  ios: '.env.ios-emulator',
  android: '.env.android-emulator',
  'ios-dotnet': '.env.ios-emulator.dotnet',
  'android-dotnet': '.env.android-emulator.dotnet',
};

function parseEnv(text) {
  /** @type {Record<string, string>} */
  const out = {};
  for (const line of text.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eq = trimmed.indexOf('=');
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    let value = trimmed.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    out[key] = value;
  }
  return out;
}

function serializeEnv(vars, headerLines = []) {
  const lines = [...headerLines];
  for (const [key, value] of Object.entries(vars)) {
    lines.push(`${key}=${value}`);
  }
  return `${lines.join('\n')}\n`;
}

function readFileIfExists(filePath) {
  if (!fs.existsSync(filePath)) return null;
  return fs.readFileSync(filePath, 'utf8');
}

function mergeMapboxFromClient(vars) {
  if (vars.VITE_MAPBOX_ACCESS_TOKEN) return vars;
  const clientRaw = readFileIfExists(clientEnvLocal);
  if (!clientRaw) return vars;
  const clientVars = parseEnv(clientRaw);
  if (clientVars.VITE_MAPBOX_ACCESS_TOKEN) {
    return { ...vars, VITE_MAPBOX_ACCESS_TOKEN: clientVars.VITE_MAPBOX_ACCESS_TOKEN };
  }
  return vars;
}

function resolveTemplateKey(platform, useDotnet) {
  if (platform === 'ios') return useDotnet ? 'ios-dotnet' : 'ios';
  if (platform === 'android') return useDotnet ? 'android-dotnet' : 'android';
  return null;
}

function main() {
  const args = process.argv.slice(2).filter((a) => a !== '--');
  const platform = args.find((a) => !a.startsWith('--'));
  const useDotnet = args.includes('--dotnet');

  if (!platform || platform === 'help' || args.includes('--help')) {
    console.log(`Usage: node scripts/use-emulator-env.mjs <ios|android|show> [--dotnet]

  ios|android     Write mobile/.env.local from the emulator template
  --dotnet        Use dotnet run ports (5032) instead of Docker (5000)
  show            Print current mobile/.env.local (if present)
`);
    process.exit(platform ? 0 : 1);
  }

  if (platform === 'show') {
    const local = path.join(mobileRoot, '.env.local');
    if (!fs.existsSync(local)) {
      console.log('No mobile/.env.local — run: npm run env:ios  or  npm run env:android');
      process.exit(1);
    }
    console.log(fs.readFileSync(local, 'utf8'));
    return;
  }

  const templateKey = resolveTemplateKey(platform, useDotnet);
  if (!templateKey) {
    console.error(`Unknown platform "${platform}". Use ios or android.`);
    process.exit(1);
  }

  const templateName = PLATFORM_FILES[templateKey];
  const templatePath = path.join(mobileRoot, templateName);
  if (!fs.existsSync(templatePath)) {
    console.error(`Missing template: ${templatePath}`);
    process.exit(1);
  }

  const header = [
    `# Generated by use-emulator-env.mjs from ${templateName}`,
    `# ${new Date().toISOString()}`,
  ];
  let vars = parseEnv(fs.readFileSync(templatePath, 'utf8'));
  vars = mergeMapboxFromClient(vars);

  const outPath = path.join(mobileRoot, '.env.local');
  fs.writeFileSync(outPath, serializeEnv(vars, header));

  console.log(`Wrote ${path.relative(process.cwd(), outPath)}`);
  console.log(`  Platform: ${platform}${useDotnet ? ' (dotnet API)' : ' (Docker API)'}`);
  console.log(`  VITE_API_BASE_URL=${vars.VITE_API_BASE_URL}`);
  if (!vars.VITE_MAPBOX_ACCESS_TOKEN) {
    console.warn(
      '  Warning: VITE_MAPBOX_ACCESS_TOKEN is empty. Set it in client/.env.local or mobile/.env.local.',
    );
  } else {
    console.log('  VITE_MAPBOX_ACCESS_TOKEN=(set)');
  }
  console.log('');
  console.log('  Next: npm run run:android   (build + sync + launch emulator)');
  console.log('        npm run check:android (verify JAVA_HOME / SDK)');
}

main();
